# Crie uma classe Retângulo com os atributos largura e altura. Adicione um método para
# calcular a área. (multiplica a base pela altura)


class Retangulo():
    def __init__(self,largura,altura):
        self.largura = largura
        self.altura = altura

    def calculo (self):
        return self.largura*self.altura
    



